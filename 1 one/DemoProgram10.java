public class DemoProgram10 {
    
    //method overloading example : same method name but different parameters 
    void m1(){
        System.out.println(" ");
    }    
    void m1(int a){
        System.out.println(a);
    }    
    void m1(int a, float b){
        System.out.println(a+" "+b);
    }    

}
